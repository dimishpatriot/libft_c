#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t	i;
	void	*ptr;

	i = 0;
	ptr = s;
	while (i < n)
	{
		*((char*)s) = c;
		s++;
		i++;
	}
	return (ptr);
}
